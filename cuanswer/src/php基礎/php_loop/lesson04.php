// ポイント：ループ文（配列に対してはforeach文がベター）を用いて各情報を表示しているか
// ポイント：それぞれ改行して表示されているか
// ポイント；配列[要素番号][キー名]で表示しているか（他の処理の場合きちんと表示されているか）
